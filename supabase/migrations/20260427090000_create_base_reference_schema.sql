create or replace function public.set_updated_at()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  nickname text,
  avatar_url text,
  signup_provider text check (
    signup_provider in ('apple', 'kakao', 'google')
  ),
  status text not null default 'active' check (
    status in ('active', 'blocked', 'deleted')
  ),
  role text not null default 'user' check (
    role in ('user', 'admin')
  ),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.profiles enable row level security;

drop trigger if exists set_profiles_updated_at on public.profiles;
create trigger set_profiles_updated_at
before update on public.profiles
for each row
execute function public.set_updated_at();

drop policy if exists "Users can view own profile" on public.profiles;
create policy "Users can view own profile"
on public.profiles
for select
to authenticated
using ((select auth.uid()) = id);

drop policy if exists "Users can update own profile" on public.profiles;
create policy "Users can update own profile"
on public.profiles
for update
to authenticated
using ((select auth.uid()) = id)
with check ((select auth.uid()) = id);

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  provider text;
begin
  provider := nullif(new.raw_app_meta_data ->> 'provider', '');

  if provider not in ('apple', 'kakao', 'google') then
    provider := null;
  end if;

  insert into public.profiles (id, nickname, avatar_url, signup_provider)
  values (
    new.id,
    coalesce(
      new.raw_user_meta_data ->> 'nickname',
      new.raw_user_meta_data ->> 'name',
      new.raw_user_meta_data ->> 'full_name'
    ),
    coalesce(
      new.raw_user_meta_data ->> 'avatar_url',
      new.raw_user_meta_data ->> 'picture'
    ),
    provider
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row
execute function public.handle_new_user();

create table if not exists public.location_types (
  id smallint primary key,
  name text not null unique,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

comment on table public.location_types is
  'Fishing location type reference data.';

alter table public.location_types enable row level security;

drop trigger if exists set_location_types_updated_at on public.location_types;
create trigger set_location_types_updated_at
before update on public.location_types
for each row
execute function public.set_updated_at();

drop policy if exists "Anyone can view location types" on public.location_types;
create policy "Anyone can view location types"
on public.location_types
for select
to anon, authenticated
using (true);

