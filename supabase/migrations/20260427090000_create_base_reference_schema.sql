create table if not exists public.fish_species (
  id integer generated by default as identity primary key,
  name text not null,
  location_type smallint not null references public.location_types(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint fish_species_name_location_type_key unique (name, location_type),
  constraint fish_species_location_type_check check (location_type in (1, 2)),
  constraint fish_species_name_not_blank check (
    char_length(btrim(name)) > 0
  )
);

comment on table public.fish_species is
  'Fish species reference data. location_type: 1=freshwater, 2=saltwater.';

comment on column public.fish_species.location_type is
  '1=freshwater, 2=saltwater';

alter table public.fish_species enable row level security;

drop trigger if exists set_fish_species_updated_at on public.fish_species;
create trigger set_fish_species_updated_at
before update on public.fish_species
for each row
execute function public.set_updated_at();

drop policy if exists "Anyone can view fish species" on public.fish_species;
create policy "Anyone can view fish species"
on public.fish_species
for select
to anon, authenticated
using (true);

